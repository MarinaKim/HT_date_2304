
#include<iostream>
#include"Date.h"

int main() {
	int n;
	cout << "Enter the task num:";
	cin >>n;
	switch (n) {
		//==
	case 1: {
		cout << "-------------------------------------------------------------------------" << endl;
		Date g(21, 02, 2017);
		Date g1(21, 03, 2017);

		cout << (g == g1) << endl;
		cout << "-------------------------------------------------------------------------" << endl;
	}break;

		//+=
	case 2: {
		Date g(21, 12, 2017);
		
		cout << "-------------------------------------------------------------------------" << endl;
		cout << "+=:" << endl;
		cout << (g += (100)) << endl;
		cout << "-------------------------------------------------------------------------" << endl;
	}break;

		//-=
	case 3: {
		Date g(1, 1, 2017);
		cout << "-------------------------------------------------------------------------" << endl;
		cout << "-=:" << endl;
		cout << (g -= (10)) << endl;
		cout << "-------------------------------------------------------------------------" << endl;
	}break;
	}
	

	system("pause");
	return 0;
}